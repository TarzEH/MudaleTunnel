    # progerss bar
    def probar(self):
        total = 0
        for value in track(range(100), description="in progerss.."):
            time.sleep(0.01)
            total += 1
        print(f"Processed {total} things")

    # the menu
    def cli_menu(
        self,
        src_port: Annotated[
            int,
            typer.Option(
                "-s",
                "--srcport",
                prompt=True,
                help="Source port that reflects the target service",
            ),
        ],
        dst_port: Annotated[
            int,
            typer.Option(
                "-d",
                "--dstport",
                prompt=True,
                help="Destination port where the target service is",
            ),
        ],
        target: Annotated[
            str, typer.Option("-t", "--target", prompt=True, help="Victim IP")
        ],
    ):
        self.spinnersquare(
            descriptiontask=f"connecting to {target} at port: {dst_port}"
        )

